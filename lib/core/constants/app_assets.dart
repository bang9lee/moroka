  // Get card image by id (0-77)
  static String getCardImage(int id) {
    if (id < 0 || id > 77) return cardFool;
    
    // Major Arcana (0-21)
    if (id <= 21) {
      final majorImages = [
        cardFool, cardMagician, cardHighPriestess, cardEmpress,
        cardEmperor, cardHierophant, cardLovers, cardChariot,
        cardStrength, cardHermit, cardWheelOfFortune, cardJustice,
        cardHangedMan, cardDeath, cardTemperance, cardDevil,
        cardTower, cardStar, cardMoon, cardSun,
        cardJudgement, cardWorld,
      ];
      return majorImages[id];
    }
    
    // Wands (22-35)
    if (id >= 22 && id <= 35) {
      final wandsImages = [
        wandsAce, wands2, wands3, wands4, wands5, wands6, wands7,
        wands8, wands9, wands10, wandsPage, wandsKnight, wandsQueen, wandsKing,
      ];
      return wandsImages[id - 22];
    }
    
    // Cups (36-49)
    if (id >= 36 && id <= 49) {
      final cupsImages = [
        cupsAce, cups2, cups3, cups4, cups5, cups6, cups7,
        cups8, cups9, cups10, cupsPage, cupsKnight, cupsQueen, cupsKing,
      ];
      return cupsImages[id - 36];
    }
    
    // Swords (50-63)
    if (id >= 50 && id <= 63) {
      final swordsImages = [
        swordsAce, swords2, swords3, swords4, swords5, swords6, swords7,
        swords8, swords9, swords10, swordsPage, swordsKnight, swordsQueen, swordsKing,
      ];
      return swordsImages[id - 50];
    }
    
    // Pentacles (64-77)
    if (id >= 64 && id <= 77) {
      final pentaclesImages = [
        pentaclesAce, pentacles2, pentacles3, pentacles4, pentacles5, pentacles6, pentacles7,
        pentacles8, pentacles9, pentacles10, pentaclesPage, pentaclesKnight, pentaclesQueen, pentaclesKing,
      ];
      return pentaclesImages[id - 64];
    }
    
    return cardFool; // Default
  }
  
  // Get spread image by type
  static String getSpreadImage(String spreadType) {
    switch (spreadType) {
      case 'oneCard':
        return spreadOneCard;
      case 'threeCard':
        return spreadThreeCard;
      case 'celticCross':
        return spreadCelticCross;
      case 'relationship':
        return spreadRelationship;
      case 'yesNo':
        return spreadYesNo;
      default:
        return spreadOneCard;
    }
  }
