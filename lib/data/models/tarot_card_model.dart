  // 전체 카드 덱
  static List<TarotCardModel> get fullDeck => [
    ...majorArcana,
    ...wandsSuit,
    ...cupsSuit,
    ...swordsSuit,
    ...pentaclesSuit,
  ];
  
  // ID로 카드 찾기
  static TarotCardModel getCardById(int id) {
    return fullDeck.firstWhere(
      (card) => card.id == id,
      orElse: () => fullDeck[0],
    );
  }
  
  // 이름으로 카드 찾기
  static TarotCardModel? getCardByName(String name) {
    try {
      return fullDeck.firstWhere(
        (card) => card.name.toLowerCase() == name.toLowerCase(),
      );
    } catch (e) {
      return null;
    }
  }
  
  // 특정 슈트의 카드들 가져오기
  static List<TarotCardModel> getCardsBySuit(CardSuit suit) {
    return fullDeck.where((card) => card.suit == suit).toList();
  }
  
  // 코트 카드만 가져오기
  static List<TarotCardModel> getCourtCards() {
    return fullDeck.where((card) => card.isCourtCard).toList();
  }
