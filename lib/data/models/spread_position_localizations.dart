  // Get localized position title
  static String getLocalizedPosition(String spreadType, String englishTitle, String locale) {
    Map<String, Map<String, String>>? positions;
    
    switch (spreadType.toLowerCase()) {
      case 'onecard':
      case 'one card':
        return oneCardPositions[locale] ?? englishTitle;
        
      case 'threecard':
      case 'three card':
      case 'three cards':
        positions = threeCardPositions;
        break;
        
      case 'celticcross':
      case 'celtic cross':
        positions = celticCrossPositions;
        break;
        
      case 'relationship':
        positions = relationshipPositions;
        break;
        
      case 'yesno':
      case 'yes/no':
        positions = yesNoPositions;
        break;
        
      default:
        return englishTitle;
    }
    
    // Find the matching position
    for (final entry in positions.entries) {
      if (entry.key == englishTitle || entry.value['en'] == englishTitle) {
        return entry.value[locale] ?? englishTitle;
      }
    }
    
    return englishTitle;
  }
