  // Get localized card name
  static String getLocalizedCardName(String englishName, String locale) {
    // Check if it's a Major Arcana card
    if (majorArcana.containsKey(englishName)) {
      return majorArcana[englishName]?[locale] ?? englishName;
    }

    // Handle Minor Arcana cards
    // Format: "Number/Court of Suit" (e.g., "Two of Cups", "Queen of Swords")
    final parts = englishName.split(' of ');
    if (parts.length == 2) {
      final rank = parts[0];
      final suit = parts[1];
      
      String localizedRank = rank;
      final String localizedSuit = suits[suit]?[locale] ?? suit;
      
      // Check if it's a court card
      if (courtCards.containsKey(rank)) {
        localizedRank = courtCards[rank]?[locale] ?? rank;
      } 
      // Check if it's a number card
      else if (numbers.containsKey(rank)) {
        localizedRank = numbers[rank]?[locale] ?? rank;
      }
      
      // Format based on language
      switch (locale) {
        case 'ko':
          return '$localizedSuit $localizedRank';
        case 'ja':
          return '$localizedSuit の $localizedRank';
        case 'zh':
          return '$localizedSuit$localizedRank';
        case 'de':
          return '$localizedRank der $localizedSuit';
        case 'es':
          return '$localizedRank de $localizedSuit';
        case 'fr':
          return '$localizedRank de $localizedSuit';
        case 'pt':
          return '$localizedRank de $localizedSuit';
        case 'hi':
          return '$localizedSuit का $localizedRank';
        case 'th':
          return '$localizedRank แห่ง$localizedSuit';
        case 'vi':
          return '$localizedRank $localizedSuit';
        case 'en':
        default:
          return '$localizedRank of $localizedSuit';
      }
    }
    
    // Return original if no translation found
    return englishName;
  }
